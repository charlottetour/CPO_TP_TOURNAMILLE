    private void showPossibleMoves(int row, int col) {
        int direction = board[row][col].isRed() ? 1 : -1;

        for (Move move : selectedCard.getMoves()) {

            int newRow = row + move.dy * direction;
            int newCol = col + move.dx;

            if (newRow < 0 || newRow >= 5 || newCol < 0 || newCol >= 5) {
                continue;
            }

            // Case vide ou capture possible
            if (board[newRow][newCol] == null
                    || board[newRow][newCol].isRed() != board[row][col].isRed()) {

                cells[newRow][newCol].setBackground(Color.GREEN);
            }
        }
    }

    private boolean isMoveAllowed(int fromRow, int fromCol, int toRow, int toCol) {
        if (selectedCard == null) {
            return false;
        }

        int direction = board[fromRow][fromCol].isRed() ? 1 : -1;

        for (Move move : selectedCard.getMoves()) {

            int targetRow = fromRow + move.dy * direction;
            int targetCol = fromCol + move.dx;

            if (targetRow == toRow && targetCol == toCol) {

                // Case vide ou capture ennemie
                return board[toRow][toCol] == null
                        || board[toRow][toCol].isRed() != board[fromRow][fromCol].isRed();
            }
        }
        return false;
    }

    private void initTurnLabel() {
        turnLabel = new JLabel("Tour : Rouge", SwingConstants.CENTER);
        turnLabel.setFont(new Font("Arial", Font.BOLD, 16));
        turnLabel.setBorder(BorderFactory.createEmptyBorder(5, 5, 5, 5));

        getContentPane().add(turnLabel, BorderLayout.NORTH);
    }

    private void updateTurnLabel() {
        turnLabel.setText(redTurn ? "Tour : Rouge" : "Tour : Bleu");
    }

    private void exchangeCards() {
        for (int i = 0; i < 2; i++) {
            if (playerCards[i] == selectedCard) {

                Card temp = middleCard;
                middleCard = playerCards[i];
                playerCards[i] = temp;
                break;
            }
        }

        selectedCard = null;
        updateCardsDisplay();

    }

    private void updateCardsDisplay() {
        for (int i = 0; i < 2; i++) {
            if (playerCards[i] != null) {
                cardButtons[i].setText(playerCards[i].getName());
                cardButtons[i].setEnabled(true);
            } else {
                cardButtons[i].setText("");
                cardButtons[i].setEnabled(false);
            }
        }

        if (middleCard != null) {
            middleCardButton.setText(middleCard.getName());
        }
    }

    private void checkVictoryAfterMove(int row, int col) {

        Piece piece = board[row][col];
        if (piece == null) {
            return;
        }

        // 1Ô∏è‚É£ Victoire par temple
        if (piece.getType() == Piece.Type.MASTER) {

            if (piece.isRed()
                    && row == BLUE_TEMPLE_ROW
                    && col == BLUE_TEMPLE_COL) {
                showWinner("Rouge");
                return;
            }

            if (!piece.isRed()
                    && row == RED_TEMPLE_ROW
                    && col == RED_TEMPLE_COL) {
                showWinner("Bleu");
                return;
            }
        }

        // 2Ô∏è‚É£ Victoire par capture du ma√Ætre
        boolean redMasterAlive = false;
        boolean blueMasterAlive = false;

        for (int r = 0; r < 5; r++) {
            for (int c = 0; c < 5; c++) {
                Piece p = board[r][c];
                if (p != null && p.getType() == Piece.Type.MASTER) {
                    if (p.isRed()) {
                        redMasterAlive = true;
                    } else {
                        blueMasterAlive = true;
                    }
                }
            }
        }

        if (!redMasterAlive) {
            showWinner("Bleu");
        }
        if (!blueMasterAlive) {
            showWinner("Rouge");
        }
    }

    private void showWinner(String winner) {
        JOptionPane.showMessageDialog(this,
                "Victoire du joueur " + winner + " !",
                "Fin de partie",
                JOptionPane.INFORMATION_MESSAGE);
        System.exit(0);
    }

    private List<Move> moves(Move... moves) {
        List<Move> list = new ArrayList<>();
        for (Move m : moves) {
            list.add(m);
        }
        return list;
    }

    private void playAITurn() {
        List<MoveAction> possibleActions = new ArrayList<>();

        // 1Ô∏è‚É£ Trouver tous les coups possibles
        for (int r = 0; r < 5; r++) {
            for (int c = 0; c < 5; c++) {

                if (board[r][c] != null && !board[r][c].isRed()) {

                    for (Card card : playerCards) {
                        if (card == null) {
                            continue;
                        }

                        int direction = -1; // bleu

                        for (Move move : card.getMoves()) {

                            int newRow = r + move.dy * direction;
                            int newCol = c + move.dx;

                            if (newRow < 0 || newRow >= 5 || newCol < 0 || newCol >= 5) {
                                continue;
                            }

                            if (board[newRow][newCol] == null
                                    || board[newRow][newCol].isRed()) {

                                possibleActions.add(
                                        new MoveAction(r, c, newRow, newCol, card)
                                );
                            }
                        }
                    }
                }
            }
        }

        // 2Ô∏è‚É£ Aucun coup possible (tr√®s rare)
        if (possibleActions.isEmpty()) {
            redTurn = true;
            updateTurnLabel();
            return;
        }

        // 3Ô∏è‚É£ Choix al√©atoire
        MoveAction action = possibleActions.get(
                (int) (Math.random() * possibleActions.size())
        );

        // 4Ô∏è‚É£ Jouer le coup
        selectedCard = action.card;
        movePiece(action.fromRow, action.fromCol, action.toRow, action.toCol);
        exchangeCards();

        // 5Ô∏è‚É£ Rendre la main au joueur
        redTurn = true;
        updateTurnLabel();
    }


