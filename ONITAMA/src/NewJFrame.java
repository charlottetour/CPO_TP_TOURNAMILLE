    private void createBoard() {

        boardPanel = new JPanel(new GridLayout(5, 5, 5, 5));
        boardPanel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        boardPanel.setBackground(Color.DARK_GRAY);

        for (int row = 0; row < 5; row++) {
            for (int col = 0; col < 5; col++) {

                JButton cell = new JButton();
                cell.setPreferredSize(new Dimension(80, 80));
                cell.setFocusPainted(false);

                if ((row + col) % 2 == 0) {
                    cell.setBackground(new Color(245, 245, 220));
                } else {
                    cell.setBackground(new Color(220, 220, 200));
                }

                final int r = row;
                final int c = col;

                cell.addActionListener(e -> onCellClicked(r, c));

                cells[row][col] = cell;
                boardPanel.add(cell);
            }
        }

        getContentPane().setLayout(new BorderLayout());
        getContentPane().add(boardPanel, BorderLayout.CENTER);
    }

    private void onCellClicked(int row, int col) {
        // 1️⃣ Sélection d’un pion
        if (selectedRow == -1 && selectedCol == -1) {

            if (board[row][col] != null) {

                // Vérifier que c’est le bon joueur
                if (board[row][col].isRed() != redTurn) {
                    return;
                }

                selectedRow = row;
                selectedCol = col;

                resetBoardColors();
                cells[row][col].setBackground(Color.ORANGE);

                // Afficher les coups possibles
                if (selectedCard != null) {
                    showPossibleMoves(row, col);
                }
            }
            return;
        }

        // 2️⃣ Tentative de déplacement
        if (isMoveAllowed(selectedRow, selectedCol, row, col)) {
            movePiece(selectedRow, selectedCol, row, col);
            exchangeCards();
            redTurn = !redTurn;
            updateTurnLabel();
        }

        resetBoardColors();
        selectedRow = -1;
        selectedCol = -1;
    }

    private void initPieces() {

        board[4][0] = new Piece(Piece.Type.STUDENT, true);
        board[4][1] = new Piece(Piece.Type.STUDENT, true);
        board[4][2] = new Piece(Piece.Type.MASTER, true);
        board[4][3] = new Piece(Piece.Type.STUDENT, true);
        board[4][4] = new Piece(Piece.Type.STUDENT, true);

        for (int col = 0; col < 5; col++) {
            cells[4][col].setText(board[4][col].getType() == Piece.Type.MASTER ? "M" : "E");
            cells[4][col].setForeground(Color.RED);
        }

        // Joueur bleu (haut)
        board[0][0] = new Piece(Piece.Type.STUDENT, false);
        board[0][1] = new Piece(Piece.Type.STUDENT, false);
        board[0][2] = new Piece(Piece.Type.MASTER, false);
        board[0][3] = new Piece(Piece.Type.STUDENT, false);
        board[0][4] = new Piece(Piece.Type.STUDENT, false);

        for (int col = 0; col < 5; col++) {
            cells[0][col].setText(board[0][col].getType() == Piece.Type.MASTER ? "M" : "E");
            cells[0][col].setForeground(Color.BLUE);
        }
    }

    private void movePiece(int fromRow, int fromCol, int toRow, int toCol) {
        // Case cible occupée → on ignore pour l’instant
        if (board[toRow][toCol] != null) {
            System.out.println("Case occupée");
            return;
        }

        // Déplacement logique
        board[toRow][toCol] = board[fromRow][fromCol];
        board[fromRow][fromCol] = null;

        // Déplacement visuel
        cells[toRow][toCol].setText(cells[fromRow][fromCol].getText());
        cells[toRow][toCol].setForeground(cells[fromRow][fromCol].getForeground());

        cells[fromRow][fromCol].setText("");

        // Réinitialiser les couleurs
        resetBoardColors();
        checkVictory(toRow, toCol);
    }

    private void resetBoardColors() {
        for (int row = 0; row < 5; row++) {
            for (int col = 0; col < 5; col++) {
                if ((row + col) % 2 == 0) {
                    cells[row][col].setBackground(new Color(245, 245, 220));
                } else {
                    cells[row][col].setBackground(new Color(220, 220, 200));
                }
            }
        }
    }

    private void initCards() {
        List<Move> tigreMoves = new ArrayList<>();
        tigreMoves.add(new Move(0, -2));
        tigreMoves.add(new Move(0, 1));
        Card tigre = new Card("Tigre", tigreMoves);

        List<Move> grueMoves = new ArrayList<>();
        grueMoves.add(new Move(0, -1));
        grueMoves.add(new Move(-1, 1));
        grueMoves.add(new Move(1, 1));
        Card grue = new Card("Grue", grueMoves);

        playerCards[0] = tigre;
        playerCards[1] = grue;

        middleCard = new Card("Cobra", new ArrayList<>());
    }

    private void createCardsPanel() {
        cardsPanel = new JPanel(new BorderLayout());

        JPanel playerPanel = new JPanel(new FlowLayout(FlowLayout.CENTER, 20, 10));

        for (int i = 0; i < 2; i++) {
            JButton btn = new JButton();
            btn.setPreferredSize(new Dimension(120, 60));

            final int index = i;
            btn.addActionListener(e -> {
                selectedCard = playerCards[index];
                if (selectedCard != null) {
                    System.out.println("Carte sélectionnée : " + selectedCard.getName());
                }
            });

            cardButtons[i] = btn;
            playerPanel.add(btn);
        }

        middleCardButton = new JButton();
        middleCardButton.setEnabled(false);
        middleCardButton.setPreferredSize(new Dimension(120, 60));

        cardsPanel.add(playerPanel, BorderLayout.SOUTH);
        cardsPanel.add(middleCardButton, BorderLayout.CENTER);

        getContentPane().add(cardsPanel, BorderLayout.SOUTH);

        updateCardsDisplay(); // ← clé

    }

    private void showPossibleMoves(int row, int col) {
        int direction = board[row][col].isRed() ? -1 : 1;

        for (Move move : selectedCard.getMoves()) {

            int newRow = row + move.dy * direction;
            int newCol = col + move.dx;

            if (newRow < 0 || newRow >= 5 || newCol < 0 || newCol >= 5) {
                continue;
            }

            // Case vide ou capture possible
            if (board[newRow][newCol] == null
                    || board[newRow][newCol].isRed() != board[row][col].isRed()) {

                cells[newRow][newCol].setBackground(Color.GREEN);
            }
        }
    }

    private boolean isMoveAllowed(int fromRow, int fromCol, int toRow, int toCol) {
        if (selectedCard == null) {
            return false;
        }

        int direction = board[fromRow][fromCol].isRed() ? -1 : 1;

        for (Move move : selectedCard.getMoves()) {

            int targetRow = fromRow + move.dy * direction;
            int targetCol = fromCol + move.dx;

            if (targetRow == toRow && targetCol == toCol) {

                // Case vide ou capture ennemie
                return board[toRow][toCol] == null
                        || board[toRow][toCol].isRed() != board[fromRow][fromCol].isRed();
            }
        }
        return false;
    }

    private void initTurnLabel() {
        turnLabel = new JLabel("Tour : Rouge", SwingConstants.CENTER);
        turnLabel.setFont(new Font("Arial", Font.BOLD, 16));
        turnLabel.setBorder(BorderFactory.createEmptyBorder(5, 5, 5, 5));

        getContentPane().add(turnLabel, BorderLayout.NORTH);
    }

    private void updateTurnLabel() {
        turnLabel.setText(redTurn ? "Tour : Rouge" : "Tour : Bleu");
    }

    private void exchangeCards() {
        selectedCard = null;
        updateCardsDisplay();

    }

    private void updateCardsDisplay() {
        for (int i = 0; i < 2; i++) {
            if (playerCards[i] != null) {
                cardButtons[i].setText(playerCards[i].getName());
                cardButtons[i].setEnabled(true);
            } else {
                cardButtons[i].setText("");
                cardButtons[i].setEnabled(false);
            }
        }

        if (middleCard != null) {
            middleCardButton.setText(middleCard.getName());
        }
    }

    private void checkVictory(int row, int col) {
        Piece piece = board[row][col];
    if (piece == null) return;

    // 1️⃣ Capture du maître adverse
    boolean redMasterAlive = false;
    boolean blueMasterAlive = false;

    for (int r = 0; r < 5; r++) {
        for (int c = 0; c < 5; c++) {
            if (board[r][c] != null &&
                board[r][c].getType() == Piece.Type.MASTER) {

                if (board[r][c].isRed()) redMasterAlive = true;
                else blueMasterAlive = true;
            }
        }
    }

    if (!redMasterAlive) {
        showWinner("Bleu");
        return;
    }

    if (!blueMasterAlive) {
        showWinner("Rouge");
        return;
    }

    // 2️⃣ Victoire par le temple
    if (piece.getType() == Piece.Type.MASTER) {

        if (piece.isRed() &&
            row == BLUE_TEMPLE_ROW && col == BLUE_TEMPLE_COL) {
            showWinner("Rouge");
        }

        if (!piece.isRed() &&
            row == RED_TEMPLE_ROW && col == RED_TEMPLE_COL) {
            showWinner("Bleu");
        }
    }
    }

    private void showWinner(String winner) {
        JOptionPane.showMessageDialog(
        this,
        "Victoire du joueur " + winner + " !",
        "Fin de partie",
        JOptionPane.INFORMATION_MESSAGE
    );
    System.exit(0);
    }


