    private void movePiece(int fromRow, int fromCol, int toRow, int toCol) {
        // Interdire de prendre une pi√®ce alli√©e
        if (board[toRow][toCol] != null
                && board[toRow][toCol].isRed() == board[fromRow][fromCol].isRed()) {
            System.out.println("Case occup√©e par un alli√©");
            return;
        }

        // D√©placement logique (capture si une pi√®ce ennemie √©tait l√†)
        board[toRow][toCol] = board[fromRow][fromCol];
        board[fromRow][fromCol] = null;

        // D√©placement visuel
        cells[toRow][toCol].setText(cells[fromRow][fromCol].getText());
        cells[toRow][toCol].setForeground(cells[fromRow][fromCol].getForeground());
        cells[fromRow][fromCol].setText("");

        resetBoardColors();

        // V√©rifier victoire apr√®s le coup
        checkVictoryAfterMove(toRow, toCol);

    }

