private void incrementer(javax.swing.JLabel label) {
    int valeur = Integer.parseInt(label.getText());
    valeur = (valeur + 1) % 10;
    label.setText(String.valueOf(valeur));
}

private void decrementer(javax.swing.JLabel label) {
    int valeur = Integer.parseInt(label.getText());
    valeur = (valeur + 9) % 10;
    label.setText(String.valueOf(valeur));
}
